<?php
// Fetch records from database
$records = array();
// Replace with your database connection and query
$records = array(
    array('id' => 1, 'title' => 'عنوان 1', 'description' => 'وصف 1', 'added_at' => '2022-01-01'),
    array('id' => 2, 'title' => 'عنوان 2', 'description' => 'وصف 2', 'added_at' => '2022-01-02'),
    array('id' => 3, 'title' => 'عنوان 3', 'description' => 'وصف 3', 'added_at' => '2022-01-03')
);

// Search functionality
if (isset($_GET['search'])) {
    $search = $_GET['search'];
    $records = array_filter($records, function($record) use ($search) {
        return strpos($record['title'], $search) !== false || strpos($record['description'], $search) !== false;
    });
}

// Output records as JSON
header('Content-Type: application/json');
echo json_encode($records);